package PD5_0;

public class PD5_2 {
    public static void main(String[] args) {
        int a;
        int b;

        for (a = 1; a <= 10; a++) {
            for (b = 1; b <= 10; b++) {
                System.out.print(a * b + "\t");
            }
            System.out.println();
        }
    }
}
